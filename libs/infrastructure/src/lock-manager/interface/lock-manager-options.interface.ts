/**
 * 락 매니저 설정 옵션 인터페이스
 * 
 * 분산 락 서비스의 동작 방식 및 연결 설정을 정의합니다.
 */
export interface LockManagerOptions {
  /**
   * Redis 호스트
   * 
   * Redis 기반 락 구현체에서 사용되는 서버 호스트 주소
   * 기본값: 'localhost'
   */
  host?: string;
  
  /**
   * Redis 포트
   * 
   * Redis 기반 락 구현체에서 사용되는 서버 포트
   * 기본값: 6379
   */
  port?: number;
  
  /**
   * Redis 비밀번호
   * 
   * Redis 기반 락 구현체에서 사용되는 인증 비밀번호
   */
  password?: string;
  
  /**
   * 락 TTL (밀리초)
   * 
   * 락이 자동으로 해제되는 시간. 데드락 방지를 위해 항상 설정하는 것이 좋습니다.
   * 락을 획득한 서비스가 비정상 종료되어도 이 시간이 지나면 락이 자동으로 해제됩니다.
   * 기본값: 30000 (30초)
   */
  lockTTL?: number;
  
  /**
   * 락 획득 시도 횟수
   * 
   * 락 획득에 실패했을 때 재시도할 최대 횟수
   * 기본값: 3
   */
  retryCount?: number;
  
  /**
   * 락 획득 재시도 간격 (밀리초)
   * 
   * 락 획득 실패 후 다시 시도하기 전 대기할 시간
   * 기본값: 200 (0.2초)
   */
  retryDelay?: number;
  
  /**
   * 락 키 접두사
   * 
   * 모든 락 키 앞에 추가되는 접두사. 다른 애플리케이션과의 충돌 방지에 유용합니다.
   * 기본값: 'lock:'
   */
  keyPrefix?: string;
} 